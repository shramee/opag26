function addmod(a: bigint, b: bigint): bigint {
  return ((a + b) % FIELD + FIELD) % FIELD
}

function mulmod(a: bigint, b: bigint): bigint {
  return (a * b) % FIELD
}

function sBox(v: bigint): bigint {
  const sq = mulmod(v, v)
  const fo = mulmod(sq, sq)
  return mulmod(fo, v)
}

function matMulExternal2(s: bigint[]): void {
  const sum = addmod(s[0], s[1])
  s[0] = addmod(sum, s[0])
  s[1] = addmod(sum, s[1])
}

function matMulInternal2(s: bigint[]): void {
  const sum = addmod(s[0], s[1])
  s[0] = addmod(s[0], sum)
  s[1] = addmod(addmod(s[1], s[1]), sum)
}

